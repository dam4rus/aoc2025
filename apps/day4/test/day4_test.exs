defmodule Day4Test do
  use ExUnit.Case
  doctest Day4

  defp input, do: "..@@.@@@@.
@@@.@.@.@@
@@@@@.@.@@
@.@@@@..@.
@@.@@@@.@@
.@@@@@@@.@
.@.@.@.@@@
@.@@@.@@@@
.@@@@@@@@.
@.@.@@@.@."

  test "input parsed as grid" do
    assert Grid.from_input(input()) == %Grid{
             grid: [
               [".", ".", "@", "@", ".", "@", "@", "@", "@", "."],
               ["@", "@", "@", ".", "@", ".", "@", ".", "@", "@"],
               ["@", "@", "@", "@", "@", ".", "@", ".", "@", "@"],
               ["@", ".", "@", "@", "@", "@", ".", ".", "@", "."],
               ["@", "@", ".", "@", "@", "@", "@", ".", "@", "@"],
               [".", "@", "@", "@", "@", "@", "@", "@", ".", "@"],
               [".", "@", ".", "@", ".", "@", ".", "@", "@", "@"],
               ["@", ".", "@", "@", "@", ".", "@", "@", "@", "@"],
               [".", "@", "@", "@", "@", "@", "@", "@", "@", "."],
               ["@", ".", "@", ".", "@", "@", "@", ".", "@", "."]
             ],
             width: 10,
             height: 10
           }
  end

  test "returns correct answer for part one" do
    assert Day4.count_accessable_roll_of_papers(input()) == 13
  end
end
